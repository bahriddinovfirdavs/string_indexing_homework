def main(s):
    """
    Given a variable s string of length five. Determine the number of digits involved in this variable.
    Args:
        s(str): parameter
    Returns:
        int: answer
    """
    a=type(s[0])
    b=type(s[1])
    c=type(s[2])
    d=type(s[3])
    e=type(s[4])
    return a,b,c,d,e

